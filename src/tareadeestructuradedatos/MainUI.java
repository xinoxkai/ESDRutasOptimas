    public void displayNodes(Aeropuerto a){
        int j;
        
        for(j=1;j<a.getCoordenadasSize();j++){
            
            int x, y;
            x=a.getCoordenada(j,0);
            y=a.getCoordenada(j,1);
            Dibujo.dibujarNodo(jPanel1.getGraphics(), x, y);
          //  jPanel1.repaint();
        }
        //jPanel1.repaint();
    }
    
    public void trazarArista(){
        int x1,y1,x2,y2;
        Imagen im= new Imagen();
        
        //en el arraylist get(0) es el primer aeropuerto seleccionado
        //y get(ruta.size()-1) es la ultima posicion
        x1=aero.getCoordenada(jComboBox1.getSelectedIndex(), 0);
        y1=aero.getCoordenada(jComboBox1.getSelectedIndex(), 1);
        
        x2=aero.getCoordenada(jComboBox2.getSelectedIndex(), 0);
        y2=aero.getCoordenada(jComboBox2.getSelectedIndex(), 1);
        
        
        if(ruta.size()==2){
            Dibujo.dibujarArista(jPanel1.getGraphics(), x1, y1, x2, y2);
            //jPanel1.repaint();
        }
        else{
            for(int s=0;s<ruta.size()-1;s++){
              
                 int num1= ruta.aerolista.get(s);
                 num1++;
                
                 int num2 = ruta.aerolista.get(s+1);
                 num2++;
                
               
                Dibujo.dibujarArista(jPanel1.getGraphics(), aero.getCoordenada(num1, 0), aero.getCoordenada(num1, 1), aero.getCoordenada(num2, 0),aero.getCoordenada(num2, 1));
             
                num1--;
                num2--;
            }
        }
    }
    
